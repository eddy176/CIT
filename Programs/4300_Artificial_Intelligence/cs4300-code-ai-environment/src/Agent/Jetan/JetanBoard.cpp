    /************************************************************/
    bool BoardLocation::operator<( const BoardLocation& rhs ) const {
      if( x < rhs.x ) {
        return true;
      } else if( x > rhs.x ) {
        return false;
      } else if( y < rhs.y ) {
        return true;
      } else if( y > rhs.y ) {
        return false;
      } else {
        return false;
      }
    }

