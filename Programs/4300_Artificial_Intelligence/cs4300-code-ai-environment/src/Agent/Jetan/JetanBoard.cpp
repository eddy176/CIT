    bool MoveData::operator<( const MoveData& rhs ) const {
      if( piece.x < rhs.piece.x ) {
        return true;
      } else if( piece.x > rhs.piece.x ) {
        return false;
      } else if( piece.y < rhs.piece.y ) {
        return true;
      } else if( piece.y > rhs.piece.y ) {
        return false;
      } else if( offset.dx < rhs.offset.dx ) {
        return true;
      } else if( offset.dx > rhs.offset.dx ) {
        return false;
      } else if( offset.dy < rhs.offset.dy ) {
        return true;
      } else if( offset.dy > rhs.offset.dy ) {
        return false;
      } else {
        return false;
      }
    }
    
