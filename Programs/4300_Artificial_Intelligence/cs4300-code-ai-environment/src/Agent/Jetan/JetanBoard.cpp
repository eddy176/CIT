    void Board::DetermineAttackedLocations( int player, SetOfBoardLocations& locations ) const {
      //locations.clear( );
      
      int offset = ( player == 1 ) ? 0 : 20;
      int piece_index;

      for( piece_index = offset; piece_index < offset + 20; piece_index++ ) {
        if( !board.pieces[ piece_index ].mAlive ) {
          continue;
        }
        if( board.pieces[ piece_index ].mType == Piece::PRINCESS ) {
          continue;
        }
        const std::vector< MovePath >* paths = &( board.pieces[ piece_index ].mPaths->getPaths( ) );
        unsigned int path_index, step_index;

        DBGP_ENV( LOG_STREAM( std::cerr ) 
                  << "piece number: " << piece_index
                  << " type: " << (int)board.pieces[ piece_index ].mType
                  << " x,y: " << (int)board.pieces[ piece_index ].mLocation.x << "," << (int)board.pieces[ piece_index ].mLocation.y
                  << " path_count: " << paths->size( )
                  << std::endl );
        
        for( path_index = 0; path_index < paths->size( ); path_index++ ) {
          const MovePath& path = (*paths)[ path_index ];
          const unsigned int step_count = path.steps.size( );
          if( step_count == 0 ) {
            DBGP_ENV( LOG_STREAM( std::cerr ) 
                      << "path.steps.size( ) == 0"
                      << std::endl );
            continue;
          }
          bool valid_move = true;

          // check intermediate steps are clear
          int column = board.pieces[ piece_index ].mLocation.x;
          int row = board.pieces[ piece_index ].mLocation.y;
          for( step_index = 0; step_index < step_count - 1; step_index++ ) {
            const BoardOffset& step = path.steps[ step_index ];
            column += step.dx;
            row    += step.dy;
            if( column < 0 || column >= 10 || row < 0 || row >= 10 ) {
              valid_move = false;
              DBGP_ENV( LOG_STREAM( std::cerr ) 
                        << "step out of bounds"
                        << std::endl );
              break;
            }
            if( board.squares[ row ][ column ] != -1 ) {
              valid_move = false;
              DBGP_ENV( LOG_STREAM( std::cerr ) 
                        << "intermediate step occupied"
                        << std::endl );
              break;
            }
          }
          // check last step
          if( valid_move ) {
            const BoardOffset& step = path.steps[ step_index ];
            column += step.dx;
            row    += step.dy;
            if( column < 0 || column >= 10 || row < 0 || row >= 10 ) {
              valid_move = false;
              DBGP_ENV( LOG_STREAM( std::cerr ) 
                        << "final step out of bounds"
                        << std::endl );
            }
            /* This is not a show stopper for showing attacked squares */
            // if( board.squares[ row ][ column ] != -1 ) {
            //   if( board.squares[ row ][ column ] >= offset &&
            //       board.squares[ row ][ column ] < offset + 20 ) {
            //     valid_move = false;
            //     DBGP_ENV( LOG_STREAM( std::cerr ) 
            //               << "final step on own piece"
            //               << std::endl );
            //   }
            // }

          }
          
          if( valid_move ) {
            BoardLocation destination( { (char)column, (char)row } );
            if( locations.count( destination ) == 0 ) {
              locations.insert( destination );
              DBGP_ENV( LOG_STREAM( std::cerr ) 
                        << "player: " << player 
                        << " attacks: " << column << "," << row
                        << std::endl );
            } else {
              DBGP_ENV( LOG_STREAM( std::cerr ) 
                        << "player: " << player 
                        << " already attacks: " << column << "," << row
                        << std::endl );
            }
          }
        }
      }
    }

    const std::vector< MoveData >& Board::DetermineLegalMoves( int player ) {
      DBGP_ENV( LOG_STREAM( std::cerr ) 
                << "player: " << player
                << std::endl );

      if( legal_moves_valid ) {
        return legal_moves;
      }
      legal_moves.resize( 0 );
      SetOfBoardLocations attacked_locations;
      int other_player = ( player == 1 ) ? 2 : 1;
      DetermineAttackedLocations( other_player, attacked_locations );

      /*
       * For each of player's live pieces, find all unique legal destinations
       */
      int offset = ( player == 1 ) ? 0 : 20;
      int piece_index;
      for( piece_index = offset; piece_index < offset + 20; piece_index++ ) {
        DBGP_ENV( LOG_STREAM( std::cerr ) 
                  << "piece number: " << piece_index
                  << std::endl );
        if( !board.pieces[ piece_index ].mAlive ) {
          continue;
        }
        bool is_princess = board.pieces[ piece_index ].mType == Piece::PRINCESS;
        SetOfBoardLocations legal_destinations;
        const std::vector< MovePath >* paths = &( board.pieces[ piece_index ].mPaths->getPaths( ) );
        if( is_princess && !board.used_escape[ player-1 ]
            && attacked_locations.count( board.pieces[ piece_index ].mLocation ) > 0 ) {
          paths = &( g_princessPieceEscapePaths.getPaths( ) );
        }
        unsigned int path_index, step_index;
        
        for( path_index = 0; path_index < paths->size( ); path_index++ ) {
          const MovePath& path = (*paths)[ path_index ];
          const unsigned int step_count = path.steps.size( );
          DBGP_ENV( LOG_STREAM( std::cerr ) 
                    << "piece path: " << path_index
                    << " steps: " << step_count
                    << std::endl );
          if( step_count == 0 ) {
            continue;
          }
          bool valid_move = true;
          // check intermediate steps are clear
          int column = board.pieces[ piece_index ].mLocation.x;
          int row = board.pieces[ piece_index ].mLocation.y;
          DBGP_ENV( LOG_STREAM( std::cerr ) 
                    << "piece loc: ( " << row << " " << column << " )"
                    << std::endl );
          for( step_index = 0; step_index < step_count - 1; step_index++ ) {
            const BoardOffset& step = path.steps[ step_index ];
            column += step.dx;
            row    += step.dy;
            DBGP_ENV( LOG_STREAM( std::cerr ) 
                      << "piece path step: " << step_index 
                      << " loc: ( " << row << " " << column << " )"
                      << std::endl );
            if( column < 0 || column >= 10 || row < 0 || row >= 10 ) {
              valid_move = false;
              break;
            }
            if( board.squares[ row ][ column ] != -1 ) {
              valid_move = false;
              break;
            }
          }
          // check last step
          if( valid_move ) {
            const BoardOffset& step = path.steps[ step_index ];
            column += step.dx;
            row    += step.dy;
            DBGP_ENV( LOG_STREAM( std::cerr ) 
                      << "piece path step: " << step_index 
                      << " dst: ( " << row << " " << column << " )"
                      << std::endl );
            if( column < 0 || column >= 10 || row < 0 || row >= 10 ) {
              DBGP_ENV( LOG_STREAM( std::cerr ) 
                        << "piece out of bounds"
                        << std::endl );
              valid_move = false;
            }
            if( board.squares[ row ][ column ] != -1 ) {
              if( board.squares[ row ][ column ] >= offset &&
                  board.squares[ row ][ column ] < offset + 20 ) {
                // ends on own piece
                DBGP_ENV( LOG_STREAM( std::cerr ) 
                          << "ends on team mate"
                          << std::endl );
                valid_move = false;
              } else if( is_princess ) {
                // ends on a piece
                DBGP_ENV( LOG_STREAM( std::cerr ) 
                          << "princess can not attack"
                          << std::endl );
                valid_move = false;
              }
            }
          }

          BoardLocation destination( { (char)column, (char)row } );
          if( is_princess && attacked_locations.count( destination ) > 0 ) {
            valid_move = false;
          }

          if( valid_move ) {
            if( legal_destinations.count( destination ) > 0 ) {
              // already have a way for this piece to get here.
              DBGP_ENV( LOG_STREAM( std::cerr ) 
                        << "non-unique destination"
                        << " dst: ( " << row << " " << column << " )"
                        << std::endl );
              valid_move = false;
            } else {
              legal_destinations.insert( destination );
            }
          }
          
          if( valid_move ) {
            MoveData move_data;
            move_data.piece.x = board.pieces[ piece_index ].mLocation.x;
            move_data.piece.y = board.pieces[ piece_index ].mLocation.y;
            move_data.offset.dx = column - board.pieces[ piece_index ].mLocation.x;
            move_data.offset.dy = row    - board.pieces[ piece_index ].mLocation.y;
            legal_moves.push_back( move_data );
            DBGP_ENV( LOG_STREAM( std::cerr ) 
                      << "added legal move " << move_data
                      << std::endl );
          }
          
        }
      }
      
      legal_moves_valid = true;
      std::sort( legal_moves.begin( ), legal_moves.end( ) );

      DBGP_ENV( LOG_STREAM( std::cerr ) 
                << "legal move count: " << legal_moves.size( )
                << std::endl );

      return legal_moves;
    }

